/* =========================================================================
   HAL slave TX callback — called from mb_slave when response is ready.
   Routes to gateway for Ethernet wrapping.
   ========================================================================= */
static void prv_MbSlaveResponseCb(const uint8_t *buf, uint16_t len, void *ctx)
{
    (void)ctx;

    /* Transmit back on RS-485 */
    /* RS-485 DE high → transmit */
    HAL_GPIO_WritePin(GW_RS485_DE_PORT, GW_RS485_DE_PIN, GPIO_PIN_SET);
    HAL_UART_Transmit(&huart3, buf, len, 100);
    HAL_GPIO_WritePin(GW_RS485_DE_PORT, GW_RS485_DE_PIN, GPIO_PIN_RESET);

    DIAG_Increment(&gDiag.mb_frames_tx);
}

/* =========================================================================
   Application entry point
   ========================================================================= */
int main(void)
{
    /* HAL and clock setup — generated by CubeMX, preserved exactly */
    HAL_Init();
    SystemClock_Config();

    /* Peripheral init */
    MX_GPIO_Init();
    MX_ETH_Init();         /* From eth.c — sets up DMA descriptors, MAC   */
    MX_USART3_UART_Init(); /* Debug / CLI UART                             */

    /* ---- Application layer init ---- */
    DIAG_Init();

    /* Modbus Slave */
    memset(s_holding_regs, 0, sizeof(s_holding_regs));
    MB_Slave_Init(GW_MB_SLAVE_ID, &s_reg_bank,
                  prv_MbSlaveResponseCb, NULL);

    /* Modbus Master (slave 1, FC 03 — adjust per deployment) */
    MB_Master_Init(1U, MB_FC_READ_HOLDING_REGS);

    /* Ethernet Gateway */
    ETH_GW_Init();

    /* Translation layer */
    Gateway_Init();

    /* CLI */
    CLI_Init();

    /* ---- Byte-level UART RX queue ---- */
    xUartByteQueue    = xQueueCreate(GW_UART_BYTE_QUEUE_LEN, sizeof(uint8_t));
    xModbusFrameQueue = xQueueCreate(GW_MODBUS_FRAME_QUEUE_LEN,
                                     sizeof(GW_ModbusFrameMsg_t));
    configASSERT(xUartByteQueue    != NULL);
    configASSERT(xModbusFrameQueue != NULL);

    /* Arm UART RX interrupt before starting scheduler */
    HAL_UART_Receive_IT(&huart3, &s_rx_byte, 1);

    /* ---- Create RTOS tasks ---- */
  //  xTaskCreate(Task_Watchdog,     "Watchdog",  GW_STACK_WATCHDOG,
  //            NULL, GW_PRIO_WATCHDOG, NULL);

    xTaskCreate(Task_UartRx,       "UartRx",    GW_STACK_UART_RX,
                NULL, GW_PRIO_UART_RX, NULL);

    xTaskCreate(Task_ModbusParser, "MbParser",  GW_STACK_MODBUS_PARSER,
                NULL, GW_PRIO_MODBUS_PARSER, NULL);

    xTaskCreate(Gateway_Task,      "Gateway",   GW_STACK_GATEWAY,
                NULL, GW_PRIO_GATEWAY, NULL);

    xTaskCreate(ETH_GW_RxTask,     "EthRx",     GW_STACK_ETH_RX,
                NULL, GW_PRIO_ETH_RX, NULL);

    xTaskCreate(ETH_GW_TxTask,     "EthTx",     GW_STACK_ETH_TX,
                NULL, GW_PRIO_ETH_TX, NULL);

    xTaskCreate(DIAG_Task,         "Diag",      GW_STACK_DIAG,
                NULL, GW_PRIO_DIAG, NULL);

    xTaskCreate(CLI_Task,          "CLI",       GW_STACK_CLI,
                NULL, GW_PRIO_CLI, NULL);

    /* Start Ethernet DMA after tasks are created */
    ETH_GW_Start();

    /* Hand control to FreeRTOS — never returns */
    vTaskStartScheduler();

    /* Should never reach here */
    Error_Handler();
    return 0;
}

/* =========================================================================
   TASK: UartRxTask
   Collects raw bytes from the ISR queue and assembles complete Modbus frames.
   Frame boundary detection: 3.5-character silence (GW_MODBUS_FRAME_TIMEOUT_MS).
   ========================================================================= */
static void Task_UartRx(void *pvParameters)
{
    (void)pvParameters;

    uint8_t             byte;
    GW_ModbusFrameMsg_t frame;
    uint16_t            frame_idx     = 0;
    TickType_t          last_rx_tick  = 0;

    memset(&frame, 0, sizeof(frame));

    for (;;)
    {
        TickType_t timeout = (frame_idx > 0)
            ? pdMS_TO_TICKS(GW_MODBUS_FRAME_TIMEOUT_MS)
            : portMAX_DELAY;

        BaseType_t got = xQueueReceive(xUartByteQueue, &byte, timeout);

        if (got == pdTRUE)
        {
            DIAG_Increment(&gDiag.uart_rx_bytes);

            if (frame_idx < GW_MODBUS_MAX_FRAME_LEN)
            {
                frame.data[frame_idx++] = byte;
                last_rx_tick = xTaskGetTickCount();
            }
            else
            {
                /* Frame too long — overflow */
                frame_idx = 0;
                DIAG_Increment(&gDiag.uart_rx_overflow);
            }
        }
        else
        {
            /* Timeout expired while mid-frame → treat as frame end */
            if (frame_idx >= MB_RTU_MIN_FRAME_LEN)
            {
                frame.length       = frame_idx;
                frame.timestamp_ms = xTaskGetTickCount();

                if (xQueueSend(xModbusFrameQueue, &frame, 0) != pdTRUE)
                    DIAG_Increment(&gDiag.queue_overflow);
            }
            frame_idx = 0;
            memset(frame.data, 0, sizeof(frame.data));
        }
    }
}

/* =========================================================================
   TASK: ModbusParserTask
   Validates CRC, classifies frame (slave request or master response),
   dispatches to the Gateway translation layer.
   ========================================================================= */
static void Task_ModbusParser(void *pvParameters)
{
    (void)pvParameters;

    GW_ModbusFrameMsg_t frame;
    GW_MbEvent_t        evt;

    for (;;)
    {
        if (xQueueReceive(xModbusFrameQueue, &frame,
                          portMAX_DELAY) != pdTRUE) continue;

        DIAG_Increment(&gDiag.mb_frames_rx);

        /* CRC validation */
        if (!MB_CRC16_Verify(frame.data, frame.length))
        {
            DIAG_Increment(&gDiag.mb_crc_errors);

            evt.type = MB_EVT_CRC_ERROR;
            Gateway_PostModbusEvent(&evt);
            continue;
        }

        /* --- Determine if this is a slave request addressed to us --- */
        uint8_t slave_id = frame.data[0];
        uint8_t fc       = frame.data[1];

        memset(&evt, 0, sizeof(evt));
        evt.slave_id      = slave_id;
        evt.function_code = fc;
        memcpy(evt.payload, frame.data, frame.length);
        evt.payload_len   = frame.length;

        if (slave_id == GW_MB_SLAVE_ID || slave_id == 0U)
        {
            /* Process as slave: mb_slave.c will call the TX callback */
            MB_Slave_ProcessFrame(frame.data, frame.length);
            evt.type = MB_EVT_SLAVE_REQUEST;
        }
        else
        {
            /* Could be a master response or a frame for another slave — forward */
            evt.type = MB_EVT_MASTER_RESPONSE;
        }

        Gateway_PostModbusEvent(&evt);
    }
}

