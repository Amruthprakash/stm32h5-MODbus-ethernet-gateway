/* =========================================================================
   UART RX ISR Callback
   Called from HAL in IRQ context — keep it fast.
   ========================================================================= */
void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart)
{
    if (huart->Instance == GW_MODBUS_UART_INSTANCE)
    {
        BaseType_t woken = pdFALSE;

        if (xQueueSendFromISR(xUartByteQueue, &s_rx_byte, &woken) != pdTRUE)
        {
            /* Safe to call Atomic_Increment from ISR */
            gDiag.uart_rx_overflow++;
        }

        /* Re-arm for next byte */
        HAL_UART_Receive_IT(&huart3, &s_rx_byte, 1);

        portYIELD_FROM_ISR(woken);
    }
}



